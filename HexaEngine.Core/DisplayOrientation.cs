namespace HexaEngine.Core
{
    /// <summary>
    /// Represents the orientation of a display.
    /// </summary>
    public enum DisplayOrientation
    {
        /// <summary>
        /// Unknown display orientation.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Landscape display orientation.
        /// </summary>
        Landscape = 1,

        /// <summary>
        /// Flipped landscape display orientation.
        /// </summary>
        LandscapeFlipped = 2,

        /// <summary>
        /// Portrait display orientation.
        /// </summary>
        Portrait = 3,

        /// <summary>
        /// Flipped portrait display orientation.
        /// </summary>
        PortraitFlipped = 4
    }
}