namespace HexaEngine.Core.Input
{
    /// <summary>
    /// Represents the available buttons on a gamepad controller.
    /// </summary>
    public enum GamepadButton
    {
        /// <summary>
        /// Button A.
        /// </summary>
        A = 0,

        /// <summary>
        /// Button B.
        /// </summary>
        B = 1,

        /// <summary>
        /// Button X.
        /// </summary>
        X = 2,

        /// <summary>
        /// Button Y.
        /// </summary>
        Y = 3,

        /// <summary>
        /// Back button.
        /// </summary>
        Back = 4,

        /// <summary>
        /// Guide button.
        /// </summary>
        Guide = 5,

        /// <summary>
        /// Start button.
        /// </summary>
        Start = 6,

        /// <summary>
        /// Left stick button.
        /// </summary>
        Leftstick = 7,

        /// <summary>
        /// Right stick button.
        /// </summary>
        Rightstick = 8,

        /// <summary>
        /// Left shoulder button.
        /// </summary>
        Leftshoulder = 9,

        /// <summary>
        /// Right shoulder button.
        /// </summary>
        Rightshoulder = 10,

        /// <summary>
        /// D-pad up button.
        /// </summary>
        DpadUp = 11,

        /// <summary>
        /// D-pad down button.
        /// </summary>
        DpadDown = 12,

        /// <summary>
        /// D-pad left button.
        /// </summary>
        DpadLeft = 13,

        /// <summary>
        /// D-pad right button.
        /// </summary>
        DpadRight = 14,

        /// <summary>
        /// Miscellaneous button 1.
        /// </summary>
        Misc1 = 0xF,

        /// <summary>
        /// Paddle 1 button.
        /// </summary>
        Paddle1 = 0x10,

        /// <summary>
        /// Paddle 2 button.
        /// </summary>
        Paddle2 = 17,

        /// <summary>
        /// Paddle 3 button.
        /// </summary>
        Paddle3 = 18,

        /// <summary>
        /// Paddle 4 button.
        /// </summary>
        Paddle4 = 19,

        /// <summary>
        /// Touchpad button.
        /// </summary>
        Touchpad = 20,
    }
}