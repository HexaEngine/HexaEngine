namespace HexaEngine.Core.Input
{
    /// <summary>
    /// Represents the state of a key or button.
    /// </summary>
    public enum KeyState
    {
        /// <summary>
        /// The key or button is in the "Up" state, indicating it is not pressed.
        /// </summary>
        Up,

        /// <summary>
        /// The key or button is in the "Down" state, indicating it is pressed.
        /// </summary>
        Down,
    }
}