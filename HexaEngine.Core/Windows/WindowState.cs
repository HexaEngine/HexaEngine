namespace HexaEngine.Core.Windows
{
    /// <summary>
    /// Represents the state of a window.
    /// </summary>
    public enum WindowState
    {
        /// <summary>
        /// The window is hidden.
        /// </summary>
        Hidden,

        /// <summary>
        /// The window is in the normal state.
        /// </summary>
        Normal,

        /// <summary>
        /// The window is minimized.
        /// </summary>
        Minimized,

        /// <summary>
        /// The window is maximized.
        /// </summary>
        Maximized,
    }
}