namespace HexaEngine.Core.Graphics
{
    /// <summary>
    /// Specifies the scaling mode for an swap-chain.
    /// </summary>
    public enum ModeScaling
    {
        /// <summary>
        /// The scaling mode is unspecified.
        /// </summary>
        Unspecified = 0,

        /// <summary>
        /// The scaling is centered.
        /// </summary>
        Centered = 1,

        /// <summary>
        /// The scaling is stretched.
        /// </summary>
        Stretched = 2
    }
}