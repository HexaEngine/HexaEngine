namespace HexaEngine.Core.Graphics.Buffers
{
    /// <summary>
    /// Specifies the format of an index used by index buffers.
    /// </summary>
    public enum IndexFormat
    {
        /// <summary>
        /// Unknown/invalid index format.
        /// </summary>
        Unknown,

        /// <summary>
        /// 16-bit unsigned integer index format.
        /// </summary>
        UInt16,

        /// <summary>
        /// 32-bit unsigned integer index format.
        /// </summary>
        UInt32,
    }
}