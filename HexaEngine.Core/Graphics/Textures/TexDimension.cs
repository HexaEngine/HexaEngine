namespace HexaEngine.Core.Graphics.Textures
{
    /// <summary>
    /// Specifies the dimensionality of a texture.
    /// </summary>
    public enum TexDimension
    {
        /// <summary>
        /// One-dimensional texture.
        /// </summary>
        Texture1D = 2,

        /// <summary>
        /// Two-dimensional texture.
        /// </summary>
        Texture2D = 3,

        /// <summary>
        /// Three-dimensional texture.
        /// </summary>
        Texture3D = 4
    }
}