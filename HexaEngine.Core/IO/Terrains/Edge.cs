namespace HexaEngine.Core.IO.Terrains
{
    /// <summary>
    /// Represents different edges in the context of terrains.
    /// </summary>
    public enum Edge
    {
        /// <summary>
        /// Positive X-axis edge.
        /// </summary>
        XPos,

        /// <summary>
        /// Negative X-axis edge.
        /// </summary>
        XNeg,

        /// <summary>
        /// Positive Z-axis edge.
        /// </summary>
        ZPos,

        /// <summary>
        /// Negative Z-axis edge.
        /// </summary>
        ZNeg,
    }
}
