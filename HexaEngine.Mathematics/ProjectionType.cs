namespace HexaEngine.Mathematics
{
    /// <summary>
    /// Represents the types of projection used in rendering.
    /// </summary>
    public enum ProjectionType
    {
        /// <summary>
        /// Perspective projection provides a realistic view with depth perception.
        /// </summary>
        Perspective,

        /// <summary>
        /// Orthographic projection provides a parallel view without perspective.
        /// </summary>
        Orthographic,
    }
}