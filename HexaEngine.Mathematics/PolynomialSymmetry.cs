namespace HexaEngine.Mathematics
{
    /// <summary>
    /// Specifies the symmetry type of a polynomial function.
    /// </summary>
    public enum PolynomialSymmetry
    {
        /// <summary>
        /// The polynomial function has no symmetry.
        /// </summary>
        None,

        /// <summary>
        /// The polynomial function is even symmetric.
        /// </summary>
        Even,

        /// <summary>
        /// The polynomial function is odd symmetric.
        /// </summary>
        Odd
    }
}